#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *cat;

	cat = s1;
	while (*s1 != '\0')
		s1++;
	while (n-- != 0 && *s2 != '\0')
		*s1++ = *s2++;
	*s1 = '\0';
	return (cat);
}
